import {
  Document,
  Page,
  View,
  Text,
  StyleSheet,
} from "@react-pdf/renderer";
import type { GradedQuestion } from "@/lib/quiz/types";

const styles = StyleSheet.create({
  page: { padding: 40, fontSize: 11, fontFamily: "Helvetica", color: "#171717" },
  brand: { fontSize: 18, fontFamily: "Helvetica-Bold" },
  sub: { color: "#666", marginTop: 2 },
  score: { fontSize: 14, fontFamily: "Helvetica-Bold", marginTop: 12, marginBottom: 8 },
  q: { marginTop: 12 },
  qText: { fontFamily: "Helvetica-Bold", marginBottom: 4 },
  opt: { marginLeft: 10, marginBottom: 2, color: "#333" },
  correct: { marginLeft: 10, marginBottom: 2, color: "#15803d", fontFamily: "Helvetica-Bold" },
  wrong: { marginLeft: 10, marginBottom: 2, color: "#b91c1c" },
  exp: { marginLeft: 10, marginTop: 3, color: "#555", fontSize: 10 },
  footer: { position: "absolute", bottom: 24, left: 40, right: 40, color: "#999", fontSize: 9 },
});

export function QuizResultPdf({
  title,
  correctCount,
  questionCount,
  graded,
}: {
  title: string;
  correctCount: number;
  questionCount: number;
  graded: GradedQuestion[];
}) {
  return (
    <Document title={`Quiz result — ${title}`}>
      <Page size="A4" style={styles.page}>
        <Text style={styles.brand}>LUMII — Quiz Result</Text>
        <Text style={styles.sub}>{title}</Text>
        <Text style={styles.score}>
          Score: {correctCount} / {questionCount}
        </Text>

        {graded.map((q, i) => (
          <View key={q.id} style={styles.q} wrap={false}>
            <Text style={styles.qText}>
              {i + 1}. {q.question}
            </Text>
            {q.options.map((opt, oi) => {
              const isCorrect = oi === q.correctAnswer;
              const isChosen = oi === q.chosen;
              const label = isCorrect
                ? " (correct answer)"
                : isChosen
                  ? " (your answer)"
                  : "";
              const style = isCorrect ? styles.correct : isChosen ? styles.wrong : styles.opt;
              return (
                <Text key={oi} style={style}>
                  {String.fromCharCode(65 + oi)}. {opt}
                  {label}
                </Text>
              );
            })}
            {q.explanation ? <Text style={styles.exp}>Why: {q.explanation}</Text> : null}
          </View>
        ))}

        <Text style={styles.footer} fixed>
          Generated by LUMII. This result is not stored after you leave the page.
        </Text>
      </Page>
    </Document>
  );
}
